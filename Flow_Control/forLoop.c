//Create a program to print numbers from 5 to 1 using a loop.
//Initialize a variable i with value 5.
//Inside the for loop, print the value of i.
//Decrease the value of i by 1 in each iteration of the loop.

#include <stdio.h>

int main() 
{
    for(int i = 5; i > 0; i--){
        printf("%d\n", i);
    }

    return 0;
}
